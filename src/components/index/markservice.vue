function scroll(){
    window.onscroll=function(){
        var topScroll =$(window).scrollTop();//滚动的距离,距离顶部的距离
        var bignav  = document.getElementById("header");//获取到导航栏id
        /*console.log(topScroll);*/
        if(topScroll > 100){  //当滚动距离大于100px时执行下面的东西
            bignav.style.position = 'fixed';
            bignav.style.top = '0';
            bignav.style.zIndex = '9999';
            /*  $("section .fu").css("margin-top","75px")*/
        }else{//当滚动距离小于100的时候执行下面的内容，也就是让导航栏恢复原状
            bignav.style.position = 'static';
            /* $("section .fu ").css("margin-top","0")*/
        }
    }
}
